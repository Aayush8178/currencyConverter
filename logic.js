const url = "https://2024-03-06.currency-api.pages.dev/v1/currencies/eur.json";

let dropdowns = document.querySelectorAll("select");
let btn = document.querySelector("button");
let frombtn = document.querySelector("#frombtn");
let tobtn = document.querySelector("#tobtn");
let input = document.querySelector("input");
let info = document.querySelector(".info");


btn.addEventListener("click", async(evt)=>{
    evt.preventDefault();
    from = frombtn.value.toLowerCase();
    to = tobtn.value.toLowerCase();
    URL = `https://2024-03-06.currency-api.pages.dev/v1/currencies/${from}.json`;
    temp_data = await fetch(URL);
    data = await(temp_data.json());
    console.log(data);
    countryfrom = data[from] ;
    amount = countryfrom[to];
    final_amount = amount* input.value;
    console.log(final_amount);
    information = `${input.value} ${frombtn.value} = ${final_amount} ${tobtn.value}` ;
    console.log(information);
    info.innerText = information;
       
})


const updateflag =(event)=>{
    CurrencyCode = event.value;
    countryCode = countryList[CurrencyCode];
    img = event.parentElement.parentElement.querySelector("img");
    newsrc = `https://flagsapi.com/${countryCode}/flat/64.png`;
    img.src = newsrc;
}


for(select of dropdowns){
    for(countrycode in countryList){
        option = document.createElement("option");
        option.innerText = countrycode;
        option.value = countrycode;
        select.append(option);
    }
    select.addEventListener("change", (evt)=>{
        updateflag(evt.target);
    });
}
































